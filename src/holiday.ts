/** 공휴일 여부 */
export function isHoliday(date: Dayjs): boolean {
  return KR_HOLIDAYS.has(date.format("YYYY-MM-DD"));
}

/** 주말 여부 (토=6, 일=0) */
export function isWeekend(date: Dayjs): boolean {
  const dow = date.day();
  return dow === 0 || dow === 6;
}

/** 영업일 여부 (주말·공휴일 아닌 날) */
export function isBusinessDay(date: Dayjs): boolean {
  return !isWeekend(date) && !isHoliday(date);
}

/** 해당 월의 첫 번째 영업일 반환 */
export function firstBusinessDayOfMonth(date: Dayjs): Dayjs {
  let d = date.startOf("month");
  while (!isBusinessDay(d)) {
    d = d.add(1, "day");
  }
  return d;
}

/** 오늘이 이번 달의 첫 번째 영업일인지 확인 */
export function isTodayFirstBusinessDay(): boolean {
  const today = dayjs();
  const first = firstBusinessDayOfMonth(today);
  return today.format("YYYY-MM-DD") === first.format("YYYY-MM-DD");
}
